import CoreGraphics
import Darwin

public extension Float {
    
    var int :Int {
        return Int(self)
    }
    
    var double :Double {
        return Double(self)
    }
    
    var cgFloat :CGFloat {
        return CGFloat(self)
    }
    
    
    
}
